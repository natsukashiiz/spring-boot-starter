package com.natsukashiiz.starter.model.response;

public class ExampleResponse {
    private String what;
    private String who;
    private String where;

    public String getWhat() {
        return what;
    }

    public ExampleResponse setWhat(String what) {
        this.what = what;
        return this;
    }

    public String getWho() {
        return who;
    }

    public ExampleResponse setWho(String who) {
        this.who = who;
        return this;
    }

    public String getWhere() {
        return where;
    }

    public ExampleResponse setWhere(String where) {
        this.where = where;
        return this;
    }
}
